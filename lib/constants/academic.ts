export const defaultSections = ["أ", "ب", "ج", "د"];

const departmentStageOverrides: Record<string, number> = {
  "هندسة العمارة": 5,
};

const collegeStageOverrides: Record<string, number> = {
  "كلية الطب": 6,
  "كلية طب الاسنان": 5,
  "كلية الطب البيطري": 5,
};

export function stagesForDepartment(department?: string | null, college?: string | null): number {
  if (department && departmentStageOverrides[department]) return departmentStageOverrides[department];
  if (college && collegeStageOverrides[college]) return collegeStageOverrides[college];
  return 4;
}

export function departmentsFor(college?: string | null): string[] {
  if (!college) return [];
  return collegeDepartments[college] ?? [];
}

export function collegeOf(department: string): string | undefined {
  for (const [college, depts] of Object.entries(collegeDepartments)) {
    if (depts.includes(department)) return college;
  }
  return undefined;
}

